// tabel perkalian

public class MultiplicationTable {

    public static void main(String[] args) {

        int num = 5;
        for(int i = 1; i <= 10; ++i)
        {
            System.out.printf("%d * %d = %d \n", num, i, num * i);
        }
    }
}


// Class digunakan untuk menciptakan suatu objek dan setiap perintah java akan di mulai dengan class
// MultiplicationTable berfungsi untuk membuat tabel perkalian
//  int num = 5; kode tersebut merupakan kode yang akan di buat menggunakan perkalian 5 
// for(int i = 1; i <= 10; ++i) digunakan untuk perkalian yang akan di mulai atau dikalikan dari angka 1 sampai dengan 10
